package com.example.fitrbackend.model;

/**
 * Represents the experience level of a user.
 */
public enum ExperienceLevel {
    /**
     * A beginner user is one who is just starting out with fitness and may not have a lot of
     * experience with working out.
     */
    BEGINNER,
    /**
     * An intermediate user is one who has some experience with working out, but may not be an
     * expert.
     */
    INTERMEDIATE,
    /**
     * An advanced user is one who is an expert in fitness and has a lot of experience with working
     * out.
     */
    ADVANCED
}